/* USER CODE BEGIN 0 */
bool HandsDetected(TIM_HandleTypeDef* htim)
{
	uint32_t pulseWidth = 0;
	if((htim->Instance->SR & TIM_SR_CC2IF) == TIM_SR_CC2IF)
	{
		pulseWidth = htim->Instance->CCR2;
	}

	if((pulseWidth >= MIN_PULSE_WIDTH) && (pulseWidth <= MAX_PULSE_WIDTH))
	{
		return true;
	}
	return false;
}

